int spmv(int nrow, const int* col, const double* val, const double* x, double* tmp, double* y) { 
    int i = 0;

#if 1 == 0
    for (i=0; i<nrow; ++i) {
        double sum = 0;
        for (int j=0; j<32; ++j) {
            sum += val[i*32+j] * x[col[i*32+j]];
        }
        y[i] = sum;
    }

#else

    asm volatile(
    " kxnorw %%k0, %%k0, %%k1                          \t\n"
    " kxnorw %%k0, %%k0, %%k2                          \t\n"
    " kxnorw %%k0, %%k0, %%k3                          \t\n"
    " kxnorw %%k0, %%k0, %%k4                          \t\n"
    " vmovdqa (%[COL]), %%ymm0                         \t\n"
    " vmovdqa 0x20(%[COL]), %%ymm1                     \t\n"
    " vmovdqa 0x40(%[COL]), %%ymm2                     \t\n"
    " vmovdqa 0x60(%[COL]), %%ymm3                     \t\n"
    " vgatherdpd (%[X],%%ymm0,8), %%zmm9%{%%k1%}       \t\n"
    " vgatherdpd (%[X],%%ymm1,8), %%zmm10%{%%k2%}      \t\n"
    " vgatherdpd (%[X],%%ymm2,8), %%zmm11%{%%k3%}      \t\n"
    " vgatherdpd (%[X],%%ymm3,8), %%zmm12%{%%k4%}      \t\n"
    " vmovdqa 0x80(%[COL]), %%ymm0                     \t\n"
    " vmovdqa 0xa0(%[COL]), %%ymm1                     \t\n"
    " vmovdqa 0xc0(%[COL]), %%ymm2                     \t\n"
    " vmovdqa 0xe0(%[COL]), %%ymm3                     \t\n"
    " vmovupd (%[VAL]), %%zmm17                        \t\n"
    " vmovupd 0x40(%[VAL]), %%zmm18                    \t\n"
    " vmovupd 0x80(%[VAL]), %%zmm19                    \t\n"
    " vmovupd 0xc0(%[VAL]), %%zmm20                    \t\n"
    " add $0x100, %[COL]                               \t\n"
    " add $0x100, %[VAL]                               \t\n"

    " sar $0x3, %[NROW]                                \t\n"
    " movl $0x4, %[I]                                  \t\n"
    " loop_spmv:                                       \t\n"

    " vmovupd (%[VAL]), %%zmm21                        \t\n"
    " vmovupd 0x40(%[VAL]), %%zmm22                    \t\n"
    " vmovupd 0x80(%[VAL]), %%zmm23                    \t\n"
    " vmovupd 0xc0(%[VAL]), %%zmm24                    \t\n"
    " kxnorw %%k0, %%k0, %%k1                          \t\n"
    " kxnorw %%k0, %%k0, %%k2                          \t\n"
    " kxnorw %%k0, %%k0, %%k3                          \t\n"
    " kxnorw %%k0, %%k0, %%k4                          \t\n"
    " prefetcht2 0x600(%[COL])                         \t\n"
    " prefetcht2 0x640(%[COL])                         \t\n"
    " vmovdqa (%[COL]), %%ymm4                         \t\n"
    " vmovdqa 0x20(%[COL]), %%ymm5                     \t\n"
    " vmovdqa 0x40(%[COL]), %%ymm6                     \t\n"
    " vmovdqa 0x60(%[COL]), %%ymm7                     \t\n"
    " vgatherdpd (%[X],%%ymm0,8), %%zmm13%{%%k1%}      \t\n"
    " vgatherdpd (%[X],%%ymm1,8), %%zmm14%{%%k2%}      \t\n"
    " vgatherdpd (%[X],%%ymm2,8), %%zmm15%{%%k3%}      \t\n"
    " vgatherdpd (%[X],%%ymm3,8), %%zmm16%{%%k4%}      \t\n"
    " vmulpd %%zmm17, %%zmm9, %%zmm8                   \t\n"
    " vfmadd231pd %%zmm18, %%zmm10, %%zmm8             \t\n"
    " vfmadd231pd %%zmm19, %%zmm11, %%zmm8             \t\n"
    " vfmadd231pd %%zmm20, %%zmm12, %%zmm8             \t\n"
    " prefetcht2 0x600(%[VAL])                         \t\n"
    " prefetcht2 0x640(%[VAL])                         \t\n"
    " prefetcht2 0x680(%[VAL])                         \t\n"
    " prefetcht2 0x6c0(%[VAL])                         \t\n"
    " vmovupd %%zmm8, (%[TMP])                         \t\n"
    " add $0x80, %[COL]                                \t\n"
    " add $0x100, %[VAL]                               \t\n"
    " add $0x40, %[TMP]                                \t\n"

    " vmovupd (%[VAL]), %%zmm17                        \t\n"
    " vmovupd 0x40(%[VAL]), %%zmm18                    \t\n"
    " vmovupd 0x80(%[VAL]), %%zmm19                    \t\n"
    " vmovupd 0xc0(%[VAL]), %%zmm20                    \t\n"
    " kxnorw %%k0, %%k0, %%k1                          \t\n"
    " kxnorw %%k0, %%k0, %%k2                          \t\n"
    " kxnorw %%k0, %%k0, %%k3                          \t\n"
    " kxnorw %%k0, %%k0, %%k4                          \t\n"
    " prefetcht2 0x600(%[COL])                         \t\n"
    " prefetcht2 0x640(%[COL])                         \t\n"
    " vmovdqa (%[COL]), %%ymm0                         \t\n"
    " vmovdqa 0x20(%[COL]), %%ymm1                     \t\n"
    " vmovdqa 0x40(%[COL]), %%ymm2                     \t\n"
    " vmovdqa 0x60(%[COL]), %%ymm3                     \t\n"
    " vgatherdpd (%[X],%%ymm4,8), %%zmm9%{%%k1%}       \t\n"
    " vgatherdpd (%[X],%%ymm5,8), %%zmm10%{%%k2%}      \t\n"
    " vgatherdpd (%[X],%%ymm6,8), %%zmm11%{%%k3%}      \t\n"
    " vgatherdpd (%[X],%%ymm7,8), %%zmm12%{%%k4%}      \t\n"
    " vmulpd %%zmm21, %%zmm13, %%zmm8                  \t\n"
    " vfmadd231pd %%zmm22, %%zmm14, %%zmm8             \t\n"
    " vfmadd231pd %%zmm23, %%zmm15, %%zmm8             \t\n"
    " vfmadd231pd %%zmm24, %%zmm16, %%zmm8             \t\n"
    " prefetcht2 0x600(%[VAL])                         \t\n"
    " prefetcht2 0x640(%[VAL])                         \t\n"
    " prefetcht2 0x680(%[VAL])                         \t\n"
    " prefetcht2 0x6c0(%[VAL])                         \t\n"
    " vmovupd %%zmm8, (%[TMP])                         \t\n"
    " add $0x80, %[COL]                                \t\n"
    " add $0x100, %[VAL]                               \t\n"
    " add $0x40, %[TMP]                                \t\n"

    " sub $0x1, %[I]                                   \t\n"
    " jnz loop_spmv                                    \t\n"
    " sub $0x200, %[TMP]                               \t\n"

    " movl $0x33, %[I]                                 \t\n"
    " kmovw %[I], %%k1                                 \t\n"
    " knotw %%k1, %%k2                                 \t\n"
    " vmovupd (%[TMP]), %%zmm4                         \t\n"
    " vmovupd 0x40(%[TMP]), %%zmm5                     \t\n"
    " vmovupd 0x80(%[TMP]), %%zmm6                     \t\n"
    " vmovupd 0xc0(%[TMP]), %%zmm7                     \t\n"
    " vmovupd 0x100(%[TMP]), %%zmm13                   \t\n"
    " vmovupd 0x140(%[TMP]), %%zmm14                   \t\n"
    " vmovupd 0x180(%[TMP]), %%zmm15                   \t\n"
    " vinsertf64x4 $0x0, %%ymm4, %%zmm13, %%zmm16      \t\n"
    " valignq $0x4, %%zmm4, %%zmm13, %%zmm13           \t\n"
    " vaddpd %%zmm13, %%zmm16, %%zmm16                 \t\n"
    " vinsertf64x4 $0x0, %%ymm5, %%zmm14, %%zmm4       \t\n"
    " valignq $0x4, %%zmm5, %%zmm14, %%zmm14           \t\n"
    " vaddpd %%zmm14, %%zmm4, %%zmm4                   \t\n"
    " vinsertf64x4 $0x0, %%ymm6, %%zmm15, %%zmm5       \t\n"
    " valignq $0x4, %%zmm6, %%zmm15, %%zmm15           \t\n"
    " vaddpd %%zmm15, %%zmm5, %%zmm5                   \t\n"
    " vinsertf64x4 $0x0, %%ymm7, %%zmm8, %%zmm6        \t\n"
    " valignq $0x4, %%zmm7, %%zmm8, %%zmm8             \t\n"
    " vaddpd %%zmm8, %%zmm6, %%zmm6                    \t\n"
    " vmovupd %%zmm5, %%zmm7                           \t\n"
    " vpermpd $0x4e, %%zmm16, %%zmm5%{%%k1%}           \t\n"
    " vpermpd $0x4e, %%zmm7, %%zmm16%{%%k2%}           \t\n"
    " vaddpd %%zmm5, %%zmm16, %%zmm7                   \t\n"
    " vmovupd %%zmm6, %%zmm5                           \t\n"
    " vpermpd $0x4e, %%zmm4, %%zmm6%{%%k1%}            \t\n"
    " vpermpd $0x4e, %%zmm5, %%zmm4%{%%k2%}            \t\n"
    " vaddpd %%zmm6, %%zmm4, %%zmm5                    \t\n"
    " vshufpd $0xaa, %%zmm5, %%zmm7, %%zmm4            \t\n"
    " vshufpd $0x55, %%zmm5, %%zmm7, %%zmm7            \t\n"
    " vaddpd %%zmm7, %%zmm4, %%zmm4                    \t\n"
    " vmovupd %%zmm4, (%[Y])                           \t\n"

    " add $0x40, %[Y]                                  \t\n"
    " movl $0x4, %[I]                                  \t\n"
    " sub $0x1, %[NROW]                                \t\n"
    " jnz loop_spmv                                    \t\n"
    : [NROW]"+r"(nrow), [COL]"+r"(col), [X]"+r"(x), [VAL]"+r"(val), [TMP]"+r"(tmp), [Y]"+r"(y), [I]"+r"(i)
    :
    : "zmm0", "zmm1", "zmm2", "zmm3", "zmm4", "zmm5", "zmm6", "zmm7", "zmm8", "zmm9", "zmm10", "zmm11", "zmm12", "zmm13", "zmm14", "zmm15", "zmm16", "zmm17", "zmm18", "zmm19", "zmm20", "zmm21", "zmm22", "zmm23", "zmm24", "k1", "k2", "k3", "k4"
    );
#endif

    return 0;
}